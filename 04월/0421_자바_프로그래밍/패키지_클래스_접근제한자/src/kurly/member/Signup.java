package kurly.member;

public class Signup {

  private String id;
  private String pw;
  private String name;
  private String email;
  private String hp;
  private String addr;
  private String gender;

  public String getId() {
    return this.id;
  }

  public void setId(String id) {
    this.id = id;
  }

  public String getPw() {
    return this.pw;
  }

  public void setPw(String pw) {
    this.pw = pw;
  }

  public String getName() {
    return this.name;
  }

  public void setName(String name) {
    this.name = name;
  }

  public String getEmail() {
    return this.email;
  }

  public void setEmail(String email) {
    this.email = email;
  }

  public String getHp() {
    return this.hp;
  }

  public void setHp(String hp) {
    this.hp = hp;
  }

  public String getAddr() {
    return this.addr;
  }

  public void setAddr(String addr) {
    this.addr = addr;
  }

  public String getGender() {
    return this.gender;
  }

  public void setGender(String gender) {
    this.gender = gender;
  }
}